    private void tableEligibilityMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEligibilityMouseClicked
        int row = tableEligibility.getSelectedRow();

        String id = tableEligibility.getModel().getValueAt(row, 0).toString();
                
        try {
            String sql = "SELECT `id`, `title`, `department`, `level`, if(`practical_mark`>0,'both','theory') AS 'type' FROM `course` WHERE `id` = '"+id+"' AND `is_deleted` =  0";
            ps = con.prepareCall(sql);
            rs = ps.executeQuery();
            if(rs.next()){
                courseTitle.setText(rs.getString("title"));
                courseId.setText(rs.getString("id"));

                eligibilityType = rs.getString("type");
                
                eligibilityCalculate(rs.getString("department"),rs.getString("level"),id);
                
                eligibilityType = "default";
            }
                        
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
                    
    }//GEN-LAST:event_tableEligibilityMouseClicked

    
