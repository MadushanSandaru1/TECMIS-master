                        pst = conn.prepareCall(sql);
                        rs = pst.executeQuery();
                        mTable.setModel(DbUtils.resultSetToTableModel(rs));
                        
                        sql = "SELECT  `course_id`, `f_theory`, `f_practical` FROM `mark` WHERE `stu_id` = '"+userStuId+"' AND `course_id` = '"+userCourseId+"'";
