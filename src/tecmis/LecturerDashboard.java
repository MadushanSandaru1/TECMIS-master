    public void calculateGPA(String cId, String sId){
        
        float quiz = 0;
        float ass = 0;
        float mid = 0;
        float theory = 0;
        float practical = 0;
        String grade = null;
        double gpa = 0;
        
        float finalMark = 0;
        
        int q = 0,a = 0,m = 0,t = 0,p = 0;
        
        try {
            cs = (com.mysql.jdbc.CallableStatement) conn.prepareCall("{call `marksForGPA`(?,?)}");
            cs.setString("sId", sId);
            cs.setString("cId", cId);
            rs = cs.executeQuery();
            
            while (rs.next()) {                
                quiz = rs.getFloat("quiz");
                ass = rs.getFloat("ass");
                mid = rs.getFloat("mid");
                theory = rs.getFloat("f_theory");
                practical = rs.getFloat("f_practical");
                
                q = rs.getInt(8);
                a = rs.getInt(9);
                m = rs.getInt(10);
                t = rs.getInt(11);
                p = rs.getInt(12);
            }
            
            if(!(q == 0)){
                finalMark += (quiz*q/100/4);
            }
            
            if(!(a == 0)){
                finalMark += (ass*a/100/3);
            }
            
            if(!(m == 0)){
                finalMark += (mid*m/100);
            }
            
            if(!(p == 0)){
                finalMark += (practical*p/100);
            }
            if(!(t == 0)){
                finalMark += (theory*t/100);
            }
            
            System.out.println(finalMark);
            
            if (finalMark>=85 && finalMark<=100) {
                grade = "A+";
                gpa = 4.0;
            } else if (finalMark>=75 && finalMark<=84) {
                grade = "A";
                gpa = 4.0;
            } else if (finalMark>=70 && finalMark<=74) {
                grade = "A-";
                gpa = 3.7;
            } else if (finalMark>=65 && finalMark<=69) {
                grade = "B+";
                gpa = 3.3;
            } else if (finalMark>=60 && finalMark<=64) {
                grade = "B";
                gpa = 3.0;
            } else if (finalMark>=55 && finalMark<=59) {
                grade = "B-";
                gpa = 2.7;
            } else if (finalMark>=50 && finalMark<=54) {
                grade = "C+";
                gpa = 2.5;
            } else if (finalMark>=45 && finalMark<=49) {
                grade = "C";
                gpa = 2.0;
            } else if (finalMark>=40 && finalMark<=44) {
                grade = "C-";
                gpa = 1.7;
            } else if (finalMark>=35 && finalMark<=39) {
                grade = "D+";
                gpa = 1.3;
            } else if (finalMark>=30 && finalMark<=34) {
                grade = "D";
                gpa = 1.0;
            } else if (finalMark>=0 && finalMark<=29) {
                grade = "F";
                gpa = 0;
            }
            
            gradeLbl.setText(grade);
            gpaLbl.setText(Double.toString(gpa));
            
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }
    
