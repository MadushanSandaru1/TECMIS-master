                String sql = "SELECT a.`id` AS 'ID', a.`f_name` AS 'First Name', a.`l_name` AS 'Last Name', a.`dob` AS 'DOB', a.`gender` AS 'Gender', a.`nic` AS 'NIC', a.`address` AS 'Address', a.`contact` AS 'Contact', a.`email` AS 'Email', a.`department` AS 'Department', b.`role` AS 'Role', b.`password` AS 'Password' FROM `"+sRole+"` a, `user` b WHERE a.`id` = b.`id` AND `department` = '"+sDept+"'";
