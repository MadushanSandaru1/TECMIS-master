    public void getEditData() {
        con = DBConnect.connect();
        
        try {
            cs = con.prepareCall("{call `SearchNoticeDetails`('"+editId+"')}");
            rs = cs.executeQuery();
            
            if(rs.next()){
                noticeNo.setText(rs.getString("Notice No"));
                noticeDate.setDate(rs.getDate("Date"));
                noticeTitle.setText(rs.getString("Notice Title"));
                notice.setText(rs.getString("Notice"));
           }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        
    }
    
