    private void btnUpdateNoticeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateNoticeActionPerformed

        noticeMessage.setText(null);

        boolean flag;

        Notice c = new Notice();

        String id = noticeNo.getText().trim();
        String title = noticeTitle.getText().trim();
        Date bd;
        String d = null;
        String content = notice.getText().trim();

        try {
            bd = noticeDate.getDate();
            d = dateFormat.format(bd);
        } catch (Exception e) {
            noticeMessage.setText("Please fill all the fields correctly");
        }
        finally{
            if(id.isEmpty() || title.isEmpty())
            noticeMessage.setText("Please fill all the fields correctly");
            else{
                c.setId(id);
                c.setDate(d);
                c.setTitle(title);
                c.setContent(content);

                flag = c.editNotice();

                if(flag){
                this.setVisible(false);
                editNoticeId = null;
            }
            }
        }
    }//GEN-LAST:event_btnUpdateNoticeActionPerformed

