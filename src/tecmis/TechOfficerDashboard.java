    private void btnUpdateProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateProfileActionPerformed
        
        Object gender = profileGender.getSelectedItem();
        Object type = profileType.getSelectedItem();

        String uid = profileId.getText().trim();
        String urole = type.toString().trim();
        String ufName = profileFirstName.getText().trim();
        String ulName = profileLastName.getText().trim();
        Date bd = null;
        String udob = null;
        String ugender;
        String unic = profileNicNo.getText().trim();
        String uaddress = profileAddress.getText().trim();
        String ucontact = profileContactNo.getText().trim();
        String uemail = profileEmail.getText().trim();
        String upassword = profilePassword.getText().trim();
        String uprofilePic = null;

        if(gender.toString().trim().equals("Male")){
            ugender = "M";
        }
        else{
            ugender = "F";
        }

        try {
            bd = profileDob.getDate();
            udob = dateFormat.format(bd);
        } catch (Exception e) {
            profileMessage.setText("Please fill all the fields correctly");
        }
        finally{
            if(ufName.isEmpty() || ulName.isEmpty() || bd == null || unic.isEmpty() || ucontact.isEmpty() || uemail.isEmpty() || upassword.isEmpty() || uaddress.isEmpty())
                profileMessage.setText("Please fill all the fields correctly");
            else if(uid.equals("ad")){
                profileMessage.setText("Enter valid user id");
            }
            else{                
                try {
                    String sql = "CALL UpdateUser('"+userID+"','"+ufName+"','"+ulName+"','"+udob+"','"+ugender+"','"+unic+"','"+uaddress+"','"+ucontact+"','"+uemail+"','"+upassword+"')";
                    ps = con.prepareCall(sql);
                    rs = ps.executeQuery();
                    JOptionPane.showMessageDialog(null, "Profile update is successfully");

                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnUpdateProfileActionPerformed

