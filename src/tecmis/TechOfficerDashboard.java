    private void btnSearchICTMedicalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchICTMedicalActionPerformed
        
        DefaultTableModel model = (DefaultTableModel) tableICTMedical.getModel();
        model.setRowCount(0);
        errorICTStuId.setText(null);
        
        String dept = "ICT";
        String level = ICTMedicalLevel.getSelectedItem().toString().trim();
        String stuId = ICTMedicalStuId.getText().trim();
        
        if(stuId.isEmpty()){
            errorICTStuId.setText("Enter Student ID");
        } else {
            try {
                ps = con.prepareCall("SELECT * FROM `student` WHERE `id` = '"+stuId+"'");
                rs = ps.executeQuery();
                if(!rs.first()){
                    errorICTStuId.setText("Invalid Student ID");
                } else {
                    try {
                        ps = con.prepareCall("CALL `GetMedical`('"+dept+"','"+level+"','"+stuId+"')");
                        rs = ps.executeQuery();
                        tableICTMedical.setModel(DbUtils.resultSetToTableModel(rs));
                        errorICTStuId.setText(null);
                        btnUpdateICTMedical.setEnabled(true);
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    }
                }
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        
    }//GEN-LAST:event_btnSearchICTMedicalActionPerformed

